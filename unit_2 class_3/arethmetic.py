x = 10
y = 3

#addition
print(x + y)  # Output: 13

#subtraction
print(x - y)  # Output: 7

#multiplication
print(x * y)  # Output: 30

#division
print(x / y)  # Output: 3.333333333

#floor division #integer division
print(x // y)  # Output: 3

#modulus #remainder
print(x % y)  # Output: 1

#exponentiation
print(x ** y)  # Output: 1000
print( 10 ** 3)  # Output: 1000
